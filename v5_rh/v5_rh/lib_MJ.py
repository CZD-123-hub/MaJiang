def split_type_s(cards=[]):
    """
    功能：手牌花色分离，将手牌分离成万条筒字各色后输出
    :param cards: 手牌　[]
    :return: 万,条,筒,字　[],[],[],[]
    """
    cards_wan = []
    cards_tiao = []
    cards_tong = []
    cards_zi = []
    for card in cards:
        if card & 0xF0 == 0x00:
            cards_wan.append(card)
        elif card & 0xF0 == 0x10:
            cards_tiao.append(card)
        elif card & 0xF0 == 0x20:
            cards_tong.append(card)
        elif card & 0xF0 == 0x30:
            cards_zi.append(card)
    return cards_wan, cards_tiao, cards_tong, cards_zi


