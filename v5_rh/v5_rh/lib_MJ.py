def is_1l_list(l):
    '''
    判断l列表中的元素是否全为list类型
    :param l:
    :return: bool
    '''
    for i in l:
        if type(i) == list:
            return False
    return True


def deepcopy(src):
    dst = []
    for i in src:
        if type(i) == list and not is_1l_list(i):  # i是列表但是i的元素不全是列表
            i = deepcopy(i)
        dst.append(copy.copy(i))
    return dst

