def get_t2info():
    '''
    计算所有的搭子，并计算对应的搭子的有效牌，同时计算出有效牌的指示下标
    :return:dzSet : 所有的搭子合集
    :return:dzEfc ： 所有的有效牌合集
    :return:efc_dzindex  :每一张有效牌对应的需要他的搭子的下标
    dzSet和dzEfc的下标是一一对应的,即搭子dzSet[i]的有效牌是dzEfc[i]所对应的列表存储的牌,又因为搭子和有效牌之间是多对多的,
    所以还需要efc_dzindex
    '''
    dzSet = [0] * (34 + 15 * 3)  # 34+8*3+7*3 对子有34种,ab搭子有8*3种,ac搭子有7*3种
    # 生成搭子有效牌表,搭子的顺序先按搭子类别、再按花色,再按搭子中第一个数的大小依次排序
    dzEfc = [0] * (34 + 15 * 3)  # 有效牌数等于搭子数量
    for i in range(len(dzSet)):
        if i <= 33:  # aa  # 34种对子
            card = int(i / 9) * 16 + i % 9 + 1  # 转换成十六进制编码的十进制表示,从1开始
            dzSet[i] = [card, card]
            dzEfc[i] = [card]
        elif i <= 33 + 8 * 3:  # ab  # ab搭子,从小到大排列
            card = int((i - 34) / 8) * 16 + (i - 34) % 8 + 1
            dzSet[i] = [card, card + 1]
            if card & 0x0f == 1:
                dzEfc[i] = [card + 2]
            elif card & 0x0f == 8:
                dzEfc[i] = [card - 1]
            else:
                dzEfc[i] = [card - 1, card + 2]
        else:  # ac
            card = int((i - 34 - 8 * 3) / 7) * 16 + (i - 34 - 8 * 3) % 7 + 1  # 针对ac中的a进行十六进制编码,从1开始

            dzSet[i] = [card, card + 2]
            dzEfc[i] = [card + 1]

    efc_dzindex = {}  # card->34+8+8+8+7+7+7

    # 计算有效牌对应的搭子索引
    cardSet = []
    for i in range(34):
        cardSet.append(i // 9 * 16 + i % 9 + 1)  # //地板除,还是要16进制编码,1-9,17-25,33-41,49-55
    for card in cardSet:
        efc_dzindex[card] = []
        # 所有牌都是对子的有效牌,这里统一处理
        efc_dzindex[card].append(translate16_33(card))  # 加aa
        color = int(card / 16)  # 0 1 2 3
        # 按牌位置情况和花色情况进行处理
        if color != 3:  # 字牌不予处理
            # 边缘位置和非边缘位置要分开处理
            if card & 0x0f == 1:  # 23
                # 例如1条,则搭子[2条,3条]需要它,位置为33+1*8+0x11&0x0f+1 = 43,刚好对应t2s第43个位置[17,18],即[0x11,0x12]
                efc_dzindex[card].append(33 + color * 8 + (card & 0x0f) + 1)
            elif card & 0x0f == 2:  # 13 34  33+8*3+color*7+···
                efc_dzindex[card].append(33 + 24 + color * 7 + (card & 0x0f) - 1)  # ac
                efc_dzindex[card].append(33 + color * 8 + (card & 0x0f) + 1)  # ab
            elif card & 0x0f == 8:  # 67 79
                efc_dzindex[card].append(33 + color * 8 + (card & 0x0f) - 2)
                efc_dzindex[card].append(33 + 24 + color * 7 + (card & 0x0f) - 1)
            elif card & 0x0f == 9:  # 78
                efc_dzindex[card].append(33 + color * 8 + (card & 0x0f) - 2)
            else:  # _bc a_c ab_ 下划线表示有效牌的位置
                efc_dzindex[card].append(33 + color * 8 + (card & 0x0f) - 2)
                efc_dzindex[card].append(33 + 24 + color * 7 + (card & 0x0f) - 1)
                efc_dzindex[card].append(33 + color * 8 + (card & 0x0f) + 1)

    return dzSet, dzEfc, efc_dzindex


